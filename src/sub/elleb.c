#include <math.h>
#include <stddef.h>

void elleb(double l, double b, double *x, double *y) {
    double d[5], e[5], f[5], rw[5], iw[5];
    double p, q, a, c, bb, bl;
    int i;

    if (x == NULL || y == NULL) {
        return;
    }

    bb = 169028.66;
    bl = 26782.5;
    a = l * 3600.0 - bl;
    c = b * 3600.0 - bb;

    d[0] = 0.68382546262761;
    e[0] = 2.363591607471e-2;
    d[1] = -3.91798328045e-8;
    e[1] = 0.0;
    d[2] = 1.4965410352e-15;
    e[2] = 4.527219881e-17;
    d[3] = -8.039471422e-23;
    e[3] = -3.89081120e-24;
    d[4] = 7.0021390e-30;
    e[4] = 2.3407700e-31;

    f[0] = 4.515344386039e1;
    f[1] = 1.17912305209e-4;
    f[2] = 5.8474201864e-10;
    f[3] = 2.73386187e-15;
    f[4] = 1.4308547e-20;

    p = 30.91849390613 * a;
    q = c * f[4];

    for (i = 4; i >= 1; --i) {
        q = c * (q + f[i]);
    }

    rw[0] = q;
    iw[0] = p;

    for (i = 1; i < 5; ++i) {
        rw[i] = q * rw[i - 1] - p * iw[i - 1];
        iw[i] = p * rw[i - 1] + q * iw[i - 1];
    }

    *y = d[4] * rw[4];
    *x = d[4] * iw[4];

    for (i = 4; i >= 1; --i) {
        *y = *y + d[i - 1] * rw[i - 1];
        *x = *x + d[i - 1] * iw[i - 1];
    }

    *y = *y / 1000.0 + 200.0;
    *x = *x / 1000.0 + 600.0;
}
